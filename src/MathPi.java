public class MathPi {

    public static void main(String[] args) {

        //System.out.println(Math.E);
        //System.out.println(Math.PI);

        double result;

        result = Math.pow(2, 5); // potencia
        result = Math.abs(-5); // valor absoluto
        result = Math.sqrt(9); // raiz quadrada
        result = Math.round(3.14); // arredondamento
        result = Math.ceil(3.14); // arredondamento pra cima (ceiling)
        result = Math.floor(3.14); // arredondamento pra baixo (floor)
        result = Math.max(10, 20); // maior número entre esses números
        result = Math.min(10, 20); // menor número entre esses números



        System.out.println(result);

    }

}
